#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*p;

	p = s1;
	while (*p != '\0')
	{
		p++;
	}
	while (*s2 != '\0')
	{
		*p++ = *s2++;
	}
	*p = '\0';
	return (s1);
}
