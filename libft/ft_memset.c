#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char		*p;
	int					i;

	i = 0;
	p = b;
	while (len > 0)
	{
		*p = c;
		p++;
		len--;
	}
	return (b);
}
