#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*p;

	p = s1;
	while (*p != '\0')
	{
		p++;
	}
	while (len-- && *s2 != '\0')
	{
		*p++ = *s2++;
	}
	*p = '\0';
	return (s1);
}
