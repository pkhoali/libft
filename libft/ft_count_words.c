#include "libft.h"

size_t	ft_count_words(const char *str, char c)
{
	int i;

	i = 0;
	while (*str)
	{
		if ((char)*str != c && (!i || (i && (char)*(str - 1) == c)))
			i++;
		++str;
	}
	return (i);
}
